#Manejo de cadenas

frase = "Loremp ipsum dolor atemmp"

print(frase.upper()) #Tranforma amyusculas
print(frase.lower()) #Tranforma minusculas
print(frase.capitalize()) #Tranforma a mayuscaula la primera letra

print(frase.isdigit()) #Pregunta si el string en su digito
print(frase.isalpha()) #Pregunta si el string es de solo letras (el espacio no es una letra)

